# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è
async def get_schedule(date=None):
    """
    –ê—Å–∏–Ω—Ö—Ä–æ–Ω–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è –Ω–∞ —É–∫–∞–∑–∞–Ω–Ω—É—é –¥–∞—Ç—É
    """
    # –ò—Å–ø–æ–ª—å–∑—É–µ–º ThreadPoolExecutor –¥–ª—è –∑–∞–ø—É—Å–∫–∞ –±–ª–æ–∫–∏—Ä—É—é—â–µ–≥–æ –∫–æ–¥–∞ –≤ –æ—Ç–¥–µ–ª—å–Ω–æ–º –ø–æ—Ç–æ–∫–µ
    import concurrent.futures
    with concurrent.futures.ThreadPoolExecutor() as pool:
        def get_schedule_blocking():
            try:
                scheduler = MosregSchedule(headless=True)  # –ò—Å–ø–æ–ª—å–∑—É–µ–º headless —Ä–µ–∂–∏–º –¥–ª—è –±–æ—Ç–∞
                lessons = scheduler.get_schedule(date)
                scheduler.close()
                return lessons
            except Exception as e:
                logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è: {e}")
                return None
        
        # –ó–∞–ø—É—Å–∫–∞–µ–º –±–ª–æ–∫–∏—Ä—É—é—â–∏–π –∫–æ–¥ –≤ –æ—Ç–¥–µ–ª—å–Ω–æ–º –ø–æ—Ç–æ–∫–µ
        return await asyncio.get_event_loop().run_in_executor(pool, get_schedule_blocking)

# –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ –≤ —Å–æ–æ–±—â–µ–Ω–∏–∏
def format_schedule(lessons, date_str):
    """
    –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ –≤ —Å–æ–æ–±—â–µ–Ω–∏–∏
    """
    if not lessons or len(lessons) == 0:
        return f"–ù–∞ {date_str} —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ –∏–ª–∏ –Ω–µ—Ç —É—Ä–æ–∫–æ–≤."
    
    message = f"üìÖ *–†–∞—Å–ø–∏—Å–∞–Ω–∏–µ –Ω–∞ {date_str}*\n\n"
    
    for i, lesson in enumerate(lessons, 1):
        message += f"*{i}. {lesson['subject']}*\n"
        
        if lesson['start_time'] != "–ù–µ —É–∫–∞–∑–∞–Ω–æ" or lesson['end_time'] != "–ù–µ —É–∫–∞–∑–∞–Ω–æ":
            time_str = f"{lesson['start_time']}"
            if lesson['end_time'] != "–ù–µ —É–∫–∞–∑–∞–Ω–æ":
                time_str += f" - {lesson['end_time']}"
            message += f"‚è∞ {time_str}\n"
        
        if lesson['room'] != "–ù–µ —É–∫–∞–∑–∞–Ω–æ":
            message += f"üö™ –ö–∞–±–∏–Ω–µ—Ç: {lesson['room']}\n"
        
        if lesson['teacher'] != "–ù–µ —É–∫–∞–∑–∞–Ω–æ":
            message += f"üë©‚Äçüè´ –£—á–∏—Ç–µ–ª—å: {lesson['teacher']}\n"
        
        message += "\n"
    
    return message

